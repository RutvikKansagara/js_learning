// Variables and Data Types

// Strings
let name = "John";

// Numbers
let age = 25;

// Booleans
let isStudent = true;

// Null
let score = null;

// Undefined
let temperature;

// Symbol (introduced in ECMAScript 6)
let uniqueId = Symbol("unique");

// Displaying Values and Types
console.log("String:", name, typeof name);
console.log("Number:", age, typeof age);
console.log("Boolean:", isStudent, typeof isStudent);
console.log("Null:", score, typeof score);
console.log("Undefined:", temperature, typeof temperature);
console.log("Symbol:", uniqueId, typeof uniqueId);

// Reassigning Variables
name = "Alice";
age = 30;
isStudent = false;

console.log("\nAfter Reassignment:");
console.log("String:", name, typeof name);
console.log("Number:", age, typeof age);
console.log("Boolean:", isStudent, typeof isStudent);
